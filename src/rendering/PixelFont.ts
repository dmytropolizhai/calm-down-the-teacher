// Latvian Ā renders as A plus a macron bar drawn above the glyph cell.
const MACRON_CHARS = new Set(['Ā']);

export type TextAlign = 'left' | 'center' | 'right';

export class PixelFont {
  constructor(private readonly r: Renderer) {}

  /** Width in logical pixels of `text` at the given pixel scale. */
  measure(text: string, scale = 1): number {
    const chars = [...text.toUpperCase()];
    if (chars.length === 0) return 0;
    return (chars.length * (W + SPACING) - SPACING) * scale;
  }

  draw(
    text: string,
    x: number,
    y: number,
    scale: number,
    color: string,
    align: TextAlign = 'left',
  ): void {
    const ctx = this.r.ctx;
    const upper = text.toUpperCase();
    const total = this.measure(upper, scale);
    let cx = x;
    if (align === 'center') cx = x - total / 2;
    else if (align === 'right') cx = x - total;
    cx = Math.round(cx);
    const py = Math.round(y);

    ctx.fillStyle = color;
    for (const ch of upper) {
      const glyphChar = MACRON_CHARS.has(ch) ? 'A' : ch;
      const rows = GLYPHS[glyphChar];
      if (rows) {
        for (let ry = 0; ry < H; ry++) {
          const row = rows[ry];
          for (let rx = 0; rx < W; rx++) {
            if (row[rx] === '1') {
              ctx.fillRect(cx + rx * scale, py + ry * scale, scale, scale);
            }
          }
        }
        if (MACRON_CHARS.has(ch)) {
          // bar one pixel-row above the glyph, spanning columns 1..3
          ctx.fillRect(cx + scale, py - scale, scale * 3, scale);
        }
      }
      cx += (W + SPACING) * scale;
    }
  }
}
